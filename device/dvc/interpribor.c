//Вывод логотипа "Интерприбор" ------------------------------------------------
//Вход: координаты верхнего левого угла
void PrintLogo75(U16 X, U16 Y) {
  U8 *Ptr=(U8*)Inter75x47; U32 Len=447;
  U16 BColor=TFT_BackgrColor, TColor=clLtBlue; //COLOR(18, 18, 31);
  U16 Color=BColor;
  U16 ClrCnt=13;
  U8 b;

  //Установка границ вывода пикселей (75*47)
  TFTSetPosRange(X, X+74, Y, Y+46);
  //Команда записи в память
  TFTWriteCmd(TFT_2C_MEMWR);

  do{
    //Чтение очередного количества одинаковых точек (фона или текста)
    b=*Ptr++;
    //Вывод b точек цвета фона или текста по горизонтали
    while(b--) TFTWriteData16(Color); //Вывод цвета на шину данных
    //Чередование цвета фона и текста
    if(Color==BColor) Color=TColor;
    else              Color=BColor;
    if(!--ClrCnt) { //Градиент цвета
      ClrCnt=13;
      if(Len>225) TColor+=COLOR(1,1,0);
      else        TColor-=COLOR(1,1,0);
    }
  }while(--Len);
}
